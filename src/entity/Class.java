package entity;

public class Class {
		private String cno;
		private String cname;
		private double credit;
		
		public String getCno() {
			return cno;
		}
		public void setCno(String cno) {
			this.cno = cno;
		}
		public String getCname() {
			return cname;
		}
		public void setCname(String cname) {
			this.cname = cname;
		}
		public double getCredit() {
			return credit;
		}
		public void setCredit(double d) {
			this.credit = d;
		}
		
		
}
